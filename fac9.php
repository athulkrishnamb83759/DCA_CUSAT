                    <h1 class="mb-4">Dr. K. V. Pramod</h1>
                    <p class="mb-4">Dr. K. V. Pramod is currently working as an Emeritus Professor in the Department of
                        Computer Applications, Cochin University of Science and Technology. He received his
                        Doctor of Philosophy from Cochin University of Science & Technology (CUSAT), Kerala,
                        India. His research interests include simulation and modeling, mathematical morphology,
                        image processing, data mining, language computing, cryptography and network security
                        and cyber forensics. In addition to a considerable amount of teaching experience, he has
                        published many research articles in peer-reviewed journals and international conferences.
                        He has produced 11 Ph.D.'s from the department, and has handled many other roles like
                        head of the department, Chairman of Board of Studies in Computer Applications, member
                        of Board of Studies in Computer Science Kerala University, member of staff council,
                        academic council, senate and so on. </p>

