                    <h1 class="mb-4">Dr. Vinod P.</h1>
                    <p class="mb-4 justify">Dr. Vinod P., is presently a Professor in the Department of Computer Applications at Cochin
                        University of Science & Technology, Cochin, Kerala. He was a Postdoctoral Researcher at the
                        Department of Mathematics, University of Padua, Italy, where he implemented the EU-H2020
                        project named TagitSmart. He was also a Postdoctoral researcher at Malaviya National Institute of
                        Technology, Jaipur, Rajasthan under the ISEA project on Mobile Security. He holds his Ph.D. in
                        Computer Engineering from Malaviya National Institute of Technology, Jaipur, India. Recently he
                        was awarded Marie Sklodowska-Curie Post Doctoral fellowship under Horizon Europe .For the
                        implementation of this project, he is approved 1,88,590.08/- Euros from the host institution of
                        research which is the University of Padua, Italy . In 2021 he was awarded the Seal of Excellence
                        for a Marie Skłodowska-Curie Individual Fellowship project proposal (Project Title OPTIMA:
                        Organization Specific Threat Intelligence Mining and Sharing ) by the European Commission. For
                        the implementation of this project, he is approved 100,000 Euros from the host institution of
                        research which is the University of Padua, Italy. He has numerous research articles published in
                        peer-reviewed Journals and International Conferences. He is a reviewer of a number of security
                        journals few to name are IEEE Transactions of Information Forensics, IEEE Communication
                        Surveys and Tutorials, Elsevier Computer Communications, and is also serving as a programme
                        committee member in the International Conferences related to Computer and Information Security.
                        Dr.Vinod’s area of interest is Adversarial Machine Learning, Malware Analysis, Context aware
                        privacy-preserving Data Mining, and Natural Language Processing.</p>

